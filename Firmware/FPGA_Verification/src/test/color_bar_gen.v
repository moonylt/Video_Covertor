//============================================================================
// Module:      color_bar_gen
// Description: HDMI 彩色条纹测试图案发生器
//              生成标准 8 色彩条图案，用于 HDMI 输出测试
//
// 彩条顺序 (从左到右):
//   白、黄、青、绿、紫、红、蓝、黑
//   (符合 SMPTE 彩条标准)
//
// 支持分辨率：720p @ 60Hz (1280x720)
// 像素时钟：74.25MHz
//
// Author:      FPGA Verification Team
// Date:        2026-03-17
// Version:     1.0
//============================================================================

`timescale 1ns / 1ps

module color_bar_gen (
    //========================================================================
    // 系统接口
    //========================================================================
    input           clk,                  // 74.25MHz 像素时钟
    input           rst_n,                // 异步复位 (低有效)
    
    //========================================================================
    // HDMI 输出接口
    //========================================================================
    output  [23:0]  hdmi_data,            // 24 位 RGB 数据
    output          hdmi_de,              // 数据使能
    output          hdmi_hs,              // 行同步
    output          hdmi_vs,              // 场同步
    
    //========================================================================
    // 状态指示
    //========================================================================
    output          active,               // 视频输出有效指示
    output          locked                // 发生器锁定指示
);

    //========================================================================
    // 本地参数定义 - 720p@60Hz 时序
    //========================================================================
    // 水平时序 (1280x720 @ 60Hz)
    localparam H_ACTIVE     = 12'd1280;   // 有效像素
    localparam H_FRONT      = 12'd110;    // 前肩
    localparam H_SYNC       = 12'd40;     // 同步脉冲
    localparam H_BACK       = 12'd220;    // 后肩
    localparam H_TOTAL      = 12'd1650;   // 水平总周期
    
    // 垂直时序
    localparam V_ACTIVE     = 11'd720;    // 有效行
    localparam V_FRONT      = 11'd5;      // 前肩
    localparam V_SYNC       = 11'd5;      // 同步脉冲
    localparam V_BACK       = 11'd20;     // 后肩
    localparam V_TOTAL      = 11'd750;    // 垂直总周期
    
    // 彩条参数
    localparam BAR_COUNT    = 8;          // 8 个彩条
    localparam BAR_WIDTH    = 12'd160;    // 每个彩条 160 像素 (1280/8)
    
    //========================================================================
    // 内部信号定义
    //========================================================================
    
    // 计数器
    reg [11:0] h_count;                   // 水平计数器
    reg [10:0] v_count;                   // 垂直计数器
    
    // 同步信号
    reg hs_reg;
    reg vs_reg;
    
    // 数据使能
    reg de_reg;
    
    // 彩条颜色 (RGB888)
    wire [23:0] bar_color;
    
    //========================================================================
    // 水平计数器
    //========================================================================
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            h_count <= 12'd0;
        end else begin
            if (h_count >= H_TOTAL - 1'b1)
                h_count <= 12'd0;
            else
                h_count <= h_count + 12'd1;
        end
    end
    
    //========================================================================
    // 垂直计数器
    //========================================================================
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            v_count <= 11'd0;
        end else begin
            if (h_count == H_TOTAL - 1'b1) begin
                if (v_count >= V_TOTAL - 1'b1)
                    v_count <= 11'd0;
                else
                    v_count <= v_count + 11'd1;
            end
        end
    end
    
    //========================================================================
    // 行同步信号生成
    //========================================================================
    // HS 在水平同步区间为低电平
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            hs_reg <= 1'b1;
        end else begin
            if (h_count >= H_ACTIVE + H_FRONT && 
                h_count < H_ACTIVE + H_FRONT + H_SYNC)
                hs_reg <= 1'b0;
            else
                hs_reg <= 1'b1;
        end
    end
    
    //========================================================================
    // 场同步信号生成
    //========================================================================
    // VS 在垂直同步区间为低电平
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            vs_reg <= 1'b1;
        end else begin
            if (v_count >= V_ACTIVE + V_FRONT && 
                v_count < V_ACTIVE + V_FRONT + V_SYNC)
                vs_reg <= 1'b0;
            else
                vs_reg <= 1'b1;
        end
    end
    
    //========================================================================
    // 数据使能信号生成
    //========================================================================
    // DE 在有效视频区域为高电平
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            de_reg <= 1'b0;
        end else begin
            if (h_count < H_ACTIVE && v_count < V_ACTIVE)
                de_reg <= 1'b1;
            else
                de_reg <= 1'b0;
        end
    end
    
    //========================================================================
    // 彩条颜色生成
    //========================================================================
    // 根据水平位置选择颜色 (组合逻辑)
    wire [2:0] bar_index = (h_count < H_ACTIVE) ? (h_count / BAR_WIDTH) : 3'd7;

    // 彩条颜色定义 (SMPTE 标准)
    // 白、黄、青、绿、紫、红、蓝、黑
    assign bar_color = 
        (bar_index == 3'd0) ? 24'hFFFFFF :  // 白色
        (bar_index == 3'd1) ? 24'hFFFF00 :  // 黄色
        (bar_index == 3'd2) ? 24'h00FFFF :  // 青色
        (bar_index == 3'd3) ? 24'h00FF00 :  // 绿色
        (bar_index == 3'd4) ? 24'hFF00FF :  // 紫色
        (bar_index == 3'd5) ? 24'hFF0000 :  // 红色
        (bar_index == 3'd6) ? 24'h0000FF :  // 蓝色
                            24'h000000;     // 黑色

    //========================================================================
    // HDMI 输出
    //========================================================================
    assign hdmi_data = de_reg ? bar_color : 24'h000000;
    assign hdmi_de   = de_reg;
    assign hdmi_hs   = hs_reg;
    assign hdmi_vs   = vs_reg;

    //========================================================================
    // 状态输出
    //========================================================================
    assign active  = de_reg;
    assign locked  = 1'b1;  // 始终锁定

endmodule

//============================================================================
// Module:      adv7125_driver
// Description: ADV7125 视频 DAC 驱动器
//              ADV7125 是三通道 10 位视频 RAMDAC
//
// 功能特性:
//   - 支持 10 位 RGB 并行数据输入
//   - 像素时钟最高 170MHz
//   - 支持 VGA/SVGA/XGA 分辨率
//
// Author:      FPGA Verification Team
// Date:        2026-03-16
// Version:     1.0
//============================================================================

`timescale 1ns / 1ps

module adv7125_driver (
    //========================================================================
    // 系统接口
    //========================================================================
    input           clk,                  // 像素时钟
    input           rst_n,                // 异步复位 (低有效)
    
    //========================================================================
    // 视频数据输入 (24 位 RGB888)
    //========================================================================
    input   [23:0]  vid_data,             // RGB888 视频数据
    input           vid_de,               // 数据使能
    input           vid_hs,               // 行同步
    input           vid_vs,               // 场同步
    
    //========================================================================
    // ADV7125 接口
    //========================================================================
    output  [9:0]   dac_red,              // 红色数据 (10 位)
    output  [9:0]   dac_green,            // 绿色数据 (10 位)
    output  [9:0]   dac_blue,             // 蓝色数据 (10 位)
    output          dac_clk,              // DAC 时钟
    output          dac_blank_n,          // 消隐控制 (低有效)
    
    //========================================================================
    // 状态输出
    //========================================================================
    output          dac_active            // DAC 工作指示
    
);

    //========================================================================
    // 内部信号定义
    //========================================================================
    
    // 数据延迟寄存器
    reg [23:0] vid_data_delay;
    reg        vid_de_delay;
    reg        vid_hs_delay;
    reg        vid_vs_delay;
    
    // RGB 数据扩展 (8 位 -> 10 位)
    wire [9:0] red_ext;
    wire [9:0] green_ext;
    wire [9:0] blue_ext;
    
    // 消隐信号
    reg        blank_reg;
    
    //========================================================================
    // 输入信号延迟 (时序对齐)
    //========================================================================
    
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            vid_data_delay <= 24'd0;
            vid_de_delay <= 1'b0;
            vid_hs_delay <= 1'b0;
            vid_vs_delay <= 1'b0;
        end else begin
            // 延迟一级以匹配输出时序
            vid_data_delay <= vid_data;
            vid_de_delay <= vid_de;
            vid_hs_delay <= vid_hs;
            vid_vs_delay <= vid_vs;
        end
    end
    
    //========================================================================
    // RGB 数据扩展 (8 位 -> 10 位)
    // 将 8 位数据扩展到 10 位：高位补 2 位 MSB
    //========================================================================
    
    // 红色通道扩展
    assign red_ext = {vid_data_delay[23:22], vid_data_delay[23:16]};
    
    // 绿色通道扩展
    assign green_ext = {vid_data_delay[15:14], vid_data_delay[15:8]};
    
    // 蓝色通道扩展
    assign blue_ext = {vid_data_delay[7:6], vid_data_delay[7:0]};
    
    //========================================================================
    // 消隐信号生成
    //========================================================================
    
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            blank_reg <= 1'b0;
        end else begin
            // 在有效视频区域外消隐
            blank_reg <= vid_de_delay;
        end
    end
    
    //========================================================================
    // 输出赋值
    //========================================================================
    
    assign dac_red = blank_reg ? red_ext : 10'd0;
    assign dac_green = blank_reg ? green_ext : 10'd0;
    assign dac_blue = blank_reg ? blue_ext : 10'd0;
    assign dac_clk = clk;
    assign dac_blank_n = blank_reg;
    assign dac_active = blank_reg;
    
endmodule
